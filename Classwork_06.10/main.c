#include <stdio.h>  // підключаємо бібліотеку для функції printf
#include <math.h>   // підключаємо бібліотеку для математичних операцій (на всякий випадок)

int main() {
    int K = 18;        // змінна K — верхня межа суми (від 0 до K)
    double S = 0.0;   // змінна S — результат (сума), ініціалізуємо нулем

    // Цикл для обчислення суми S = ∑ (3i + 4) / i²
    // Починаємо з i = 1, щоб уникнути ділення на 0
    for (int i = 1; i <= K; i++) { // тіло циклу — команди, які повторюються
        // Додаємо до суми черговий елемент (3i + 4) / (i²) та зберігає результат
        S += (3.0 * i + 4.0) / (i * i);
    }

    // Виводимо результат на екран з 6 знаками після коми
    printf("S = %.6f\n", S);

    return 0;  // завершення програми
}
